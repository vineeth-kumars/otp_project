import React from "react";
import PropTypes from "prop-types";
import SwipeableViews from "react-swipeable-views";
import { useTheme } from "@mui/material/styles";
import AppBar from "@mui/material/AppBar";
import Tabs from "@mui/material/Tabs";
import Tab from "@mui/material/Tab";
import Typography from "@mui/material/Typography";
import Box from "@mui/material/Box";
import Contacts from "./components/ContactsTab";
import MessageHistoryPage from "./components/MessageHistoryPage";
import { createContext } from "react";

export const messageHistoryContext = createContext(null);

function TabPanel(props) {
  const { children, value, index, ...other } = props;

  return (
    <div
      role="tabpanel"
      hidden={value !== index}
      id={`full-width-tabpanel-${index}`}
      aria-labelledby={`full-width-tab-${index}`}
      {...other}
    >
      {value === index && (
        <Box sx={{ p: 3 }}>
          <Typography>{children}</Typography>
        </Box>
      )}
    </div>
  );
}

TabPanel.propTypes = {
  children: PropTypes.node,
  index: PropTypes.number.isRequired,
  value: PropTypes.number.isRequired
};

function a11yProps(index) {
  return {
    id: `full-width-tab-${index}`,
    "aria-controls": `full-width-tabpanel-${index}`
  };
}

export default function FullWidthTabs() {
  const theme = useTheme();
  const [value, setValue] = React.useState(0);

  const handleChange = (event, newValue) => {
    setValue(newValue);
  };

  const handleChangeIndex = (index) => {
    setValue(index);
  };
const [sentMessageHistory,setSentMessageHistory] = React.useState([])
  
  const messageHistory = [];
  const setMessageHistory = (e) =>{
    messageHistory.push(e);
    setSentMessageHistory(messageHistory);
    console.log(messageHistory)
  }
 
  return (
    <Box sx={{ bgcolor: "background.paper", width: 500 }}>
      <AppBar position="static">
        <Tabs
          value={value}
          onChange={handleChange}
          indicatorColor="secondary"
          textColor="inherit"
          variant="fullWidth"
          aria-label="full width tabs example"
        >
          <Tab label="Contacts" {...a11yProps(0)} />
          <Tab label="Message History" {...a11yProps(1)} />
        </Tabs>
      </AppBar>
      <SwipeableViews
        axis={theme.direction === "rtl" ? "x-reverse" : "x"}
        index={value}
        onChangeIndex={handleChangeIndex}
      >
        <TabPanel value={value} index={0} dir={theme.direction}>
          
          <Contacts  setMessageHistory={setMessageHistory}/>
          
        </TabPanel>
        <TabPanel value={value} index={1} dir={theme.direction}>
        
        <messageHistoryContext.Provider value={{sentMessageHistory}}>
          <MessageHistoryPage
          //  messageHistory={messageHistory}
           />
          </messageHistoryContext.Provider>
        </TabPanel>
      </SwipeableViews>
    </Box>
  );
}
